package com.example.vit_lib;

public class UploadBio {
    public String name;
    public String url;

    public UploadBio() {
    }

    public UploadBio(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }
    public String getUrl() {
        return url;
    }

}
