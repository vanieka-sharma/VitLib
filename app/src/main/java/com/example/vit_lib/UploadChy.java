package com.example.vit_lib;

public class UploadChy {
    public String name;
    public String url;

    public UploadChy() {
    }

    public UploadChy(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }
    public String getUrl() {
        return url;
    }

}
